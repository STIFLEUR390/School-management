    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::findOrFail($id);
        $salary_log = EmployeeSallaryLog::where('employee_id',$user->id)->get();

        return view('backend.employee.employee_salary.employee_salary_details', compact('user', 'salary_log'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::findOrFail($id);

        return view('backend.employee.employee_salary.employee_salary_increment', compact('user'));
